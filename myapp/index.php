        const urlParams = new URLSearchParams(window.location.search);
        const error = urlParams.get('error');
        if (error === 'login_failed') {
            toast({
                title: "Login Failed",
                content: "Your password is incorrect",
                typeicon: "error",
                duration: 8000 
            })  
        }else if(error === 'query_error'){
            toast({
                title: "query_error",
                content: "An error occurred while querying",
                typeicon: "error",
                duration: 8000 
            })  
        }
        else if(error === 'Invalid_pass'){
            toast({
                title: "Invalid password",
                content: "Your password is invalid, possibly too short",
                typeicon: "error",
                duration: 8000 
            })  
        }
        else if(error === 'Invalid_email'){
            toast({
                title: "Invalid email",
                content: "Your email is invalid",
                typeicon: "error",
                duration: 8000 
            })  
        }
        else if(error === 'empty_data'){
            toast({
                title: "Empty data",
                content: "Please fill in the information",
                typeicon: "error",
                duration: 8000 
            })  
        }
        else if(error === 'failed'){
            toast({
                title: "Error",
                content: "An error has occurred",
                typeicon: "error",
                duration: 8000 
            })  
        }
        
    </script>
	
